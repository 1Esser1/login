// ── Language picker button ────────────────────────────────────────────────────

const LANG_OPTIONS = [
  { code: 'en', label: 'EN' },
  { code: 'fr', label: 'FR' },
  { code: 'ar', label: 'AR' },
];

// ── Main page ─────────────────────────────────────────────────────────────────

export default function ReportPage() {
  const { theme } = useTheme();
  const navigate  = useNavigate();
  const { user }  = useAuthStore();
  const { language: globalLang } = useLanguage();

  // Report language is independent from the global UI language
  const [reportLang, setReportLang] = useState(globalLang);
  const [translating, setTranslating] = useState(false);
  const tx = useReportTx(reportLang);

  const [loading, setLoading]     = useState(true);
  const [aiLoading, setAiLoading] = useState(false);
  const [error, setError]         = useState('');
  const [tasks, setTasks]         = useState([]);
  const [subtasks, setSubtasks]   = useState([]);
  const [slaItems, setSlaItems]   = useState([]);
  const [members, setMembers]     = useState([]);
  const [narrative, setNarrative] = useState(null);

  // Watch for translation completion
  useEffect(() => {
    if (reportLang !== 'en') setTranslating(true);
    const timer = setTimeout(() => setTranslating(false), 3000);
    return () => clearTimeout(timer);
  }, [reportLang]);

  useEffect(() => {
    const fetchAll = async () => {
      try {
        const [allTasks, boardData, slaData, workloadData] = await Promise.all([
          taskService.getAllTasks(),
          sprintService.getBoard(),
          slaService.getAll(),
          sprintService.getWorkload(),
        ]);
        const t = allTasks || [];
        const s = boardData?.subtasks || [];
        const sl = slaData || [];
        const m = workloadData?.members || [];

        setTasks(t);
        setSubtasks(s);
        setSlaItems(sl);
        setMembers(m);

        // Build stats for AI
        const scoredTasks   = t.filter(x => x.finalScore != null);
        const approvedTasks = t.filter(x => x.status === 'APPROVED');
        const topTasks      = scoredTasks.sort((a, b) => b.finalScore - a.finalScore)
                              .slice(0, 5)
                              .map(x => ({ title: x.title, score: x.finalScore?.toFixed(1), moscow: x.moscowLabel ?? 'N/A' }));
        const sprintDone    = s.filter(x => x.status === 'DONE').length;
        const slaOk         = sl.filter(x => x.slaStatus === 'ON_TRACK' || x.slaStatus === 'COMPLETED').length;

        const stats = {
          totalTasks: t.length, scoredTasks: scoredTasks.length, approvedTasks: approvedTasks.length,
          topTasks,
          sprintTotal: s.length, sprintTodo: s.filter(x => x.status === 'TODO').length,
          sprintInProgress: s.filter(x => x.status === 'IN_PROGRESS').length, sprintDone,
          sprintPct: s.length > 0 ? Math.round((sprintDone / s.length) * 100) : 0,
          slaTotal: sl.length, slaOnTrack: sl.filter(x => x.slaStatus === 'ON_TRACK').length,
          slaAtRisk: sl.filter(x => x.slaStatus === 'AT_RISK').length,
          slaOverdue: sl.filter(x => x.slaStatus === 'OVERDUE').length,
          slaCompliancePct: sl.length > 0 ? Math.round((slaOk / sl.length) * 100) : 0,
          teamTotal: m.length,
          teamAvailable: m.filter(x => x.totalEstimatedHours === 0).length,
          teamModerate: m.filter(x => x.totalEstimatedHours >= 20 && x.totalEstimatedHours < 35).length,
          teamHeavy: m.filter(x => x.totalEstimatedHours >= 35).length,
          teamTotalHours: m.reduce((sum, x) => sum + x.totalEstimatedHours, 0).toFixed(0),
        };

        setAiLoading(true);
        try {
          const aiRes = await api.post('/api/report/summary', stats);
          setNarrative(aiRes.data);
        } catch { /* use null narrative — will show fallback text */ }
        setAiLoading(false);

      } catch (e) {
        setError('Failed to load report data. Make sure the backend is running.');
      } finally {
        setLoading(false);
      }
    };
    fetchAll();
  }, []);

  if (loading) {
    return (
      <div style={{
        marginLeft: '240px', minHeight: '100vh', display: 'flex',
        flexDirection: 'column', alignItems: 'center', justifyContent: 'center',
        backgroundColor: theme.pageBg, gap: '1rem',
      }}>
        <style>{PRINT_CSS}</style>
        <RefreshCw size={32} color="#CC2027" style={{ animation: 'spin 1s linear infinite' }} />
        <p style={{ color: theme.textSub, fontWeight: '600', fontSize: '0.875rem' }}>Loading report data…</p>
        <style>{`@keyframes spin { to { transform: rotate(360deg); } }`}</style>
      </div>
    );
  }

  if (error) {
    return (
      <div style={{ marginLeft: '240px', minHeight: '100vh', padding: '3rem', backgroundColor: theme.pageBg }}>
        <div style={{ display: 'flex', alignItems: 'center', gap: '0.75rem', backgroundColor: '#FEF2F2', border: '1px solid #FECACA', borderRadius: '0.75rem', padding: '1rem 1.25rem' }}>
          <AlertCircle size={18} color="#DC2626" />
          <p style={{ color: '#DC2626', fontSize: '0.875rem' }}>{error}</p>
        </div>
      </div>
    );
  }

  return (
    <div style={{ marginLeft: '240px', backgroundColor: theme.tagBg, minHeight: '100vh', paddingBottom: '3rem' }}>
      <style>{PRINT_CSS}</style>
      <style>{`@keyframes spin { to { transform: rotate(360deg); } }`}</style>

      {/* Controls bar — hidden when printing */}
      <div className="no-print" style={{
        position: 'sticky', top: 0, zIndex: 50,
        backgroundColor: '#1A1A2E',
        padding: '0.875rem 2rem',
        display: 'flex', alignItems: 'center', justifyContent: 'space-between',
      }}>
        <div style={{ display: 'flex', alignItems: 'center', gap: '1rem' }}>
          <button onClick={() => navigate(-1)} style={{
            display: 'flex', alignItems: 'center', gap: '0.4rem',
            padding: '0.4rem 0.875rem', borderRadius: '0.4rem',
            border: '1px solid rgba(255,255,255,0.15)', backgroundColor: 'transparent',
            color: '#9CA3AF', fontSize: '0.8rem', cursor: 'pointer',
          }}>
            <ArrowLeft size={14} /> Back
          </button>
          <span style={{ color: 'white', fontWeight: '700', fontSize: '0.875rem' }}>
            IT Management Report
          </span>
          {aiLoading && (
            <span style={{ display: 'flex', alignItems: 'center', gap: '0.35rem', fontSize: '0.72rem', color: '#A78BFA' }}>
              <RefreshCw size={11} style={{ animation: 'spin 1s linear infinite' }} />
              AI generating narrative…
            </span>
          )}
          {translating && (
            <span style={{ display: 'flex', alignItems: 'center', gap: '0.35rem', fontSize: '0.72rem', color: '#6EE7B7' }}>
              <RefreshCw size={11} style={{ animation: 'spin 1s linear infinite' }} />
              Translating report…
            </span>
          )}
        </div>

        <div style={{ display: 'flex', alignItems: 'center', gap: '0.75rem' }}>
          {/* Report language picker */}
          <div style={{ display: 'flex', alignItems: 'center', gap: '0.5rem' }}>
            <span style={{ color: '#6B7280', fontSize: '0.72rem' }}>Report language:</span>
            <div style={{ display: 'flex', backgroundColor: 'rgba(255,255,255,0.08)', borderRadius: '0.4rem', padding: '0.15rem', gap: '0.15rem' }}>
              {LANG_OPTIONS.map(({ code, label }) => (
                <button
                  key={code}
                  onClick={() => setReportLang(code)}
                  style={{
                    padding: '0.3rem 0.625rem', borderRadius: '0.3rem', border: 'none',
                    cursor: 'pointer', fontSize: '0.72rem', fontWeight: '700',
                    backgroundColor: reportLang === code ? '#CC2027' : 'transparent',
                    color: reportLang === code ? 'white' : '#9CA3AF',
                    transition: 'all 0.15s',
                  }}
                >
                  {label}
                </button>
              ))}
            </div>
          </div>

          <button
            onClick={() => window.print()}
            style={{
              display: 'flex', alignItems: 'center', gap: '0.5rem',
              padding: '0.55rem 1.25rem', borderRadius: '0.5rem',
              border: 'none', backgroundColor: '#CC2027',
              color: 'white', fontSize: '0.825rem', fontWeight: '700',
              cursor: 'pointer',
            }}
          >
            <Printer size={15} /> Print / Save as PDF
          </button>
        </div>
      </div>

      {/* Tip bar */}
      <div className="no-print" style={{
        backgroundColor: '#F5F3FF', borderBottom: '1px solid #DDD6FE',
        padding: '0.6rem 2rem', fontSize: '0.72rem', color: '#6D28D9',
        display: 'flex', alignItems: 'center', gap: '0.5rem',
      }}>
        <span>✦</span>
        <span>
          <strong>Print tip:</strong> In the print dialog, select <strong>A4</strong> paper, set margins to <strong>None</strong>, and enable <strong>Background graphics</strong> to preserve colors.
        </span>
      </div>

      {/* Report pages */}
      <div style={{ padding: '2.5rem 2rem 0' }}>
        <CoverPage user={user} tasks={tasks} subtasks={subtasks} slaItems={slaItems} members={members} tx={tx} />
        <ExecutiveSummaryPage narrative={narrative} tx={tx} />
        <TaskPriorityPage tasks={tasks} tx={tx} />
        <SprintPage subtasks={subtasks} narrative={narrative} tx={tx} />
        <SlaPageSection slaItems={slaItems} narrative={narrative} tx={tx} />
        <WorkloadPageSection members={members} tx={tx} />
      </div>
    </div>
  );
}
