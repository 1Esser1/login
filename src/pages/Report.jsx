// ── Per-report translation hook (independent from global language) ───────────
function useReportTx(lang) {
  const [tx, setTx] = useState(REPORT_STRINGS);

  useEffect(() => {
    if (lang === 'en') { setTx(REPORT_STRINGS); return; }
    const cacheKey = `priorit_report_tx_${lang}`;
    try {
      const cached = JSON.parse(localStorage.getItem(cacheKey) || '{}');
      if (Object.keys(cached).length >= Object.keys(REPORT_STRINGS).length) {
        setTx(cached);
        return;
      }
    } catch {}
    translateBatch(Object.values(REPORT_STRINGS), lang).then(results => {
      const translated = {};
      Object.keys(REPORT_STRINGS).forEach((k, i) => {
        translated[k] = results[i] || REPORT_STRINGS[k];
      });
      setTx(translated);
      try { localStorage.setItem(`priorit_report_tx_${lang}`, JSON.stringify(translated)); } catch {}
    });
  }, [lang]);

  return tx;
}

